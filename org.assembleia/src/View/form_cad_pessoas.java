    /**
     * Metodo que faz a vericação se todos os campos do formulario foram preenchidos
     * @param componentes
     * @return boolean
     */
    private boolean verificarCampos(Component[] componentes) {
        boolean preenchido = true;
        for (int i = 0; i < componentes.length; i++) {
            Component componente = componentes[i];
            if (componente instanceof JTextField) {
                if (((JTextField) componente).getText().isEmpty()) {
                    ((JTextField) componente).setBorder(new LineBorder(java.awt.Color.RED));
                    preenchido = false;
                } else {
                    // campo preenchido... ok
                    ((JTextField) componente).setBorder(new LineBorder(Color.LIGHT_GRAY));
                    preenchido = true;
                }
            }
        }
        return preenchido;
    }

    /**
     * metodo criado para tratar os valores corretamente
     *
     * @param valor_entrada
     */
    private double convertValorInfo(String valor_entrada) {

        // transfroma em vetor
        IntStream s = valor_entrada.chars();
        double valor = 0;

        // verifica a qtd de posiçoes... indica numeros com casas de milhares
        if (s.count() > 6) {
            String n = valor_entrada.replace(".", "");
            String b = n.replace(",", ".");

            valor = Double.parseDouble(b);
//            System.out.println(valor);

        } else {

            // valores com casas abaixo de milhares
            String n = valor_entrada.replace(",", ".");
            valor = Double.parseDouble(n);
//            System.out.println(n);
        }

        return valor;
    }

