    private void btn_salvar_lancamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvar_lancamentoActionPerformed
        int opt = JOptionPane.showConfirmDialog(this, "Salvar as informações no banco de dados?");

        if (opt == 0) {

            if (verificarCampos(formulario.getComponents())) {

                // verificando o tipo de cadastro: CNPJ ou CPF
                if (radio_cpf.isSelected()) {

                    // recuperando dados CPF
                    pessoas_model pessoaFisica = new pessoas_model();
                    pessoaFisica.setNome(campo_nome.getText());
                    pessoaFisica.setCpf(campo_cpf_cnpj.getText());
                    pessoaFisica.setData_cadastro(campo_data_lancamento.getText());

                    pessoas_control inserir = new pessoas_control();
                    inserir.inserirPessoasEmpresas(pessoaFisica, tipo_pessoas.CPF);

                } else if (radio_cnpj.isSelected()) {

                    // recuperando dados CNPJ
                    pessoas_model pessoaJuridica = new pessoas_model();
                    pessoaJuridica.setNome(campo_nome.getText());
                    pessoaJuridica.setCnpj(campo_cpf_cnpj.getText());
                    pessoaJuridica.setData_cadastro(campo_data_lancamento.getText());

                    pessoas_control inserir = new pessoas_control();
                    inserir.inserirPessoasEmpresas(pessoaJuridica, tipo_pessoas.CNPJ);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Atenção: verifique se todos os campos foram preenchidos", "Mensagem", JOptionPane.ERROR_MESSAGE);

            }

        }

    }//GEN-LAST:event_btn_salvar_lancamentoActionPerformed

