    private void btn_salvar_alteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvar_alteracaoActionPerformed
        // atualiza as informações
        pessoas_model pessoaEmpresa = new pessoas_model();
        pessoas_control control = new pessoas_control();

        if (this.tipoPessoa == tipo_pessoas.CNPJ) {

            pessoaEmpresa.setNome(campo_nome.getText());
            pessoaEmpresa.setCnpj(campo_cnpj_cpf.getText());

            control.atualizarCadastrPessoa(pessoaEmpresa, tipoPessoa, idPessoa);

        } else if (this.tipoPessoa == tipo_pessoas.CPF) {

            pessoaEmpresa.setNome(campo_nome.getText());
            pessoaEmpresa.setCpf(campo_cnpj_cpf.getText());

            control.atualizarCadastrPessoa(pessoaEmpresa, tipoPessoa, idPessoa);
        }
    }//GEN-LAST:event_btn_salvar_alteracaoActionPerformed

