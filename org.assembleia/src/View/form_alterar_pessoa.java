    /**
     * preenche os campos de acordo com o id selecionado na tela de localização
     *
     * @param id_pessoa
     * @param tipo
     */
    private void preencherCampos(int id_pessoa, int tipo) {

        if (tipo == tipo_pessoas.CNPJ) {

            pessoas_model juridica;
            pessoas_control recuperar = new pessoas_control();
            juridica = recuperar.capturarInfoPessoasEmpresa(id_pessoa);

            radio_cnpj.setEnabled(true);
//            radio_cpf.setEnabled(true);
            radio_cnpj.setSelected(true);
            lbl_tipo_cnpj_cpf.setText("CNPJ:");
            campo_nome.setText(juridica.getNome());
            campo_cnpj_cpf.setText(juridica.getCnpj());

            btn_salvar_alteracao.setEnabled(true);
//            System.out.println("tipo pessoa: " + this.tipoPessoa);

        } else if (tipo == tipo_pessoas.CPF) {

            pessoas_model fisica;
            pessoas_control control = new pessoas_control();
            fisica = control.capturarInfoPessoasEmpresa(id_pessoa);

            radio_cpf.setEnabled(true);
//            radio_cnpj.setEnabled(true);
            radio_cpf.setSelected(true);
            lbl_tipo_cnpj_cpf.setText("CPF:");
            campo_nome.setText(fisica.getNome());
            campo_cnpj_cpf.setText(fisica.getCpf());

            btn_salvar_alteracao.setEnabled(true);
//            System.out.println("tipo pessoa: " + this.tipoPessoa);

        }

    }

