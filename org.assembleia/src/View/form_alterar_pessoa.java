    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        form_localizar_pessoas localizar = new form_localizar_pessoas(null, true);
        localizar.setVisible(true);

        if (localizar.codigo_pessoa != 0) {

            preencherCampos(localizar.codigo_pessoa, localizar.tipo_cpf_cnpj);
            this.idPessoa = localizar.codigo_pessoa;

            // aqui indicamos qual metodo iremos utilizar na atualização
            // se será pessoa JURIDICA ou FISICA
            this.tipoPessoa = localizar.tipo_cpf_cnpj;

        } else {
            JOptionPane.showMessageDialog(this, "Ops... nenhuma pessoa foi selecinada", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

